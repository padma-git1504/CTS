class data_type_demo{
    public static void main(String args[]){
        int num1  = 20;
        float num2 = 19.55f;
        double num3 = 30.95;
        char ch = 'c';
        boolean val = true;
        System.out.println("Integer type : "+num1);
        System.out.println("Float type : "+num2);
        System.out.println("Double type : "+num3);
        System.out.println("Character type : "+ch);
        System.out.println("Boolean type : "+val);
    }
}